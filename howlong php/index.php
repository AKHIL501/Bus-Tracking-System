              <p style="text-align: justify">The bus arrival time is primary information to most city
transport travelers. Excessively long waiting time at bus
stops often discourages the travelers and makes them re-
luctant to take buses. In this paper, we present a bus ar-
rival time prediction system based on bus passengers? par-
ticipatory sensing. With commodity mobile phones, the bus
passengers? surrounding environmental context is effectively
collected and utilized to estimate the bus traveling routes
and predict bus arrival time at various bus stops. The pro-
posed system solely relies on the collaborative effort of the
participating users and is independent from the bus operat-
ing companies, so it can be easily adopted to support uni-
versal bus service systems without requesting support from
particular bus operating companies. Instead of referring to
GPS enabled location information, we resort to more gener-
ally available and energy efficient sensing resources, includ-
ing cell tower signals, movement statuses, audio recordings,
etc., which bring less burden to the participatory party and
encourage their participation. We develop a prototype sys-
tem with different types of Android based mobile phones
and comprehensively experiment over a 7 week period. The
evaluation results suggest that the proposed system achieves
outstanding prediction accuracy compared with those bus
company initiated and GPS supported solutions. At the
same time, the proposed solution is more generally available
and energy friendly</p>
